package entity;

public class Money {
    public float payNum;
    public String payTime;

    public float getPayNum() {
        return payNum;
    }

    public void setPayNum(float payNum) {
        this.payNum = payNum;
    }

    public String getPayTime() {
        return payTime;
    }

    public void setPayTime(String payTime) {
        this.payTime = payTime;
    }
}
